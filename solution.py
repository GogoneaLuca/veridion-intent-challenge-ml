class IntentParser:
    def __init__(self, client: Groq, model_name: str):
        self.client = client
        self.model_name = model_name

    def parse(self, user_query: str) -> dict:
        schema_json = SearchIntent.model_json_schema()
        try:
            res = self.client.chat.completions.create(
                messages=[
                    {"role": "system", "content": f"Ești un sistem strict de extragere a intenției de căutare. Răspunde STRICT cu JSON valid: {json.dumps(schema_json)}"},
                    {"role": "user", "content": f"Query: '{user_query}'"}
                ],
                model=self.model_name, temperature=0.0, response_format={"type": "json_object"}
            )
            return SearchIntent(**json.loads(res.choices[0].message.content)).model_dump()
        except Exception as e:
            print(f"Intent Parser Error: {e}")
            return None

class FastRetriever:
    def __init__(self, df: pd.DataFrame):
        self.df = df
        self.embedding_model = SentenceTransformer('all-MiniLM-L6-v2')

    def retrieve(self, intent: dict, top_k: int = 15) -> list:
        filters = intent.get('hard_filters', {})
        sem_query = intent.get('semantic_query', "")

        mask = pd.Series(True, index=self.df.index)

        if filters.get('locations'):
            valid_countries = [loc.lower() for loc in filters['locations']]
            mask = mask & (self.df['country_code'].isin(valid_countries) | self.df['country_code'].isna())

        if filters.get('min_employees'):
            mask = mask & ((self.df['employee_count'] >= filters['min_employees']) | self.df['employee_count'].isna())
        if filters.get('max_employees'):
            mask = mask & ((self.df['employee_count'] <= filters['max_employees']) | self.df['employee_count'].isna())
        if filters.get('min_revenue'):
            mask = mask & ((self.df['revenue'] >= filters['min_revenue']) | self.df['revenue'].isna())
        if filters.get('min_founded_year'):
            mask = mask & ((self.df['year_founded'] >= filters['min_founded_year']) | self.df['year_founded'].isna())
        if filters.get('is_public') is not None:
            mask = mask & (self.df['is_public'] == filters['is_public'])

        filtered_df = self.df[mask].copy()
        if filtered_df.empty:
            return []

        if sem_query:
            q_emb = self.embedding_model.encode(sem_query, convert_to_tensor=True)
            c_emb = self.embedding_model.encode(filtered_df['description'].tolist(), convert_to_tensor=True)
            filtered_df['semantic_score'] = util.cos_sim(q_emb, c_emb)[0].cpu().numpy()
            filtered_df = filtered_df.sort_values(by='semantic_score', ascending=False)

        top_df = filtered_df.head(top_k)
        return top_df[['operational_name', 'year_founded', 'employee_count', 'description']].to_dict('records')

class DeepJudge:
    def __init__(self, client: Groq, model_name: str):
        self.client = client
        self.model_name = model_name

    def rank(self, user_query: str, candidates: list) -> list:
        if not candidates:
            return []

        schema_json = EvaluationList.model_json_schema()
        companies_text = "\n".join([f"- Nume: {c['operational_name']}, An: {c['year_founded']}, Angajați: {c['employee_count']}\n  Descriere: {c['description'][:350]}" for c in candidates])

        system_prompt = f"""
        Ești un analist de business. Evaluează candidații față de cerința utilizatorului.
        Răspunde STRICT cu JSON valid: {json.dumps(schema_json)}

        GRILA DE EVALUARE (Tiers):
        [90-100] Potrivire Perfectă: Toate filtrele stricte și intenția semantică respectate clar.
        [70-89] Potrivire Bună: Intenția semantică este atinsă. Lipsesc date numerice (nan), dar descrierea sugerează o potrivire.
        [40-69] Potrivire Parțială: Industria are legătură, dar ratează flagrant un filtru major.
        [0-39] Irelevant: Nu are legătură cu intenția principală.

        REGULI STRICTE DE REDACTARE:
        1. NU folosi note rotunde (multipli de 10). Folosește note specifice (ex: 83, 94, 76, 42) pentru departajare fină.
        2. Fii direct în 'reasoning'. Scrie sub formă de notițe scurte. EVITĂ limbajul repetitiv.
        3. Scrie 'reasoning' MAI ÎNTÂI, apoi dă nota!
        """

        try:
            res = self.client.chat.completions.create(
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": f"Cerința: '{user_query}'\n\nCompanii:\n{companies_text}"}
                ],
                model=self.model_name, temperature=0.0, response_format={"type": "json_object"}
            )
            evals = EvaluationList(**json.loads(res.choices[0].message.content)).evaluations
            evals.sort(key=lambda x: x.relevance_score, reverse=True)
            return [e.model_dump() for e in evals]
        except Exception as e:
            print(f"Reranker Error: {e}")
            return []

class VeridionSearchEngine:
    def __init__(self, data_path: str, api_key: str):
        self.client = Groq(api_key=api_key)
        self.llm_model = "llama-3.3-70b-versatile"

        try:
            with open(data_path, 'r', encoding='utf-8') as f:
                df = pd.read_json(f, lines=True)
        except FileNotFoundError:
            raise FileNotFoundError(f"Fisierul de date nu a fost gasit la calea: {data_path}")

        self.parser = IntentParser(self.client, self.llm_model)
        self.retriever = FastRetriever(df)

        self.retriever.df['country_code'] = self.retriever.df['address'].apply(
            lambda x: ast.literal_eval(x).get('country_code', '').lower() if isinstance(x, str) else (x.get('country_code', '').lower() if isinstance(x, dict) else None)
        )
        self.retriever.df['year_founded'] = pd.to_numeric(self.retriever.df['year_founded'], errors='coerce')
        self.retriever.df['employee_count'] = pd.to_numeric(self.retriever.df['employee_count'], errors='coerce')
        self.retriever.df['revenue'] = pd.to_numeric(self.retriever.df['revenue'], errors='coerce')
        self.retriever.df['description'] = self.retriever.df['description'].fillna("")

        self.judge = DeepJudge(self.client, self.llm_model)

    def search(self, user_query: str) -> list:
        intent = self.parser.parse(user_query)
        if not intent:
            return []

        candidates = self.retriever.retrieve(intent, top_k=10)
        return self.judge.rank(user_query, candidates)

