class HardFilters(BaseModel):
    locations: List[str] = Field(default=[], description="OBLIGATORIU: Returnează DOAR coduri de țară ISO de 2 litere (ex: 'us', 'ro', 'ch', 'fr'). Dacă userul cere o regiune, returnează lista cu codurile țărilor majore din acea regiune.")
    min_employees: Optional[int] = Field(default=None)
    max_employees: Optional[int] = Field(default=None)
    min_revenue: Optional[int] = Field(default=None)
    max_revenue: Optional[int] = Field(default=None)
    min_founded_year: Optional[int] = Field(default=None)
    is_public: Optional[bool] = Field(default=None)

class SearchIntent(BaseModel):
    hard_filters: HardFilters
    semantic_query: str = Field(description="Frază scurtă (2-6 cuvinte) pentru Vector Search.")

class CompanyEvaluation(BaseModel):
    operational_name: str
    reasoning: str = Field(description="Analizează pas cu pas compatibilitatea firmei cu intenția și filtrele.")
    relevance_score: int = Field(description="Scor de la 0 la 100 bazat pe grila de evaluare.")

class EvaluationList(BaseModel):
    evaluations: List[CompanyEvaluation]

